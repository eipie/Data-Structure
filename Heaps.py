# Max heap property
    # the key of a node is greater or equal to the keys of its children
# Min heap property
    # the key ofa node is less or equal to the keys of its children

# Some functions of a priority que
# Insert(S, x): insert element x into S
# Max(S): return element of S with the largest key
# Extract(S): return element of S with the largest key and remove it
# Increase Key(S, x, k): increase the value of x's key to new value k 

